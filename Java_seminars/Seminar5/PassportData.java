package Seminar5;

// Создать структуру для хранения Номеров паспортов и Фамилий сотрудников организации.
// 123456 Иванов 321456 Васильев 234561 Петрова 234432 Иванов 654321 Петрова 345678 Иванов
// Вывести данные по сотрудникам с фамилией Иванов.

// Создание структуры данных (HashMap): Мы используем HashMap, чтобы хранить номера паспортов в качестве ключей и фамилии сотрудников в качестве значений.
// Добавление данных в структуру: Каждая запись содержит номер паспорта в качестве ключа и фамилию сотрудника в качестве значения.
// Вывод данных по сотрудникам с фамилией "Иванов": Мы проходим по каждой записи в HashMap и проверяем, равна ли фамилия сотрудника "Иванов". Если да, мы выводим номер паспорта и фамилию сотрудника.


import java.util.HashMap;
import java.util.Map;


public class PassportData {
    public static void main(String[] args) {
        // Создание структуры для хранения номеров паспортов и фамилий сотрудников
        Map<String, String> passportData = new HashMap<>(); // Создаем HashMap с ключом (номер паспорта) и значением (фамилия сотрудника)
        
        // Добавление данных в структуру
        passportData.put("123456", "Иванов"); // Номер паспорта "123456", Фамилия "Иванов"
        passportData.put("321456", "Васильев");
        passportData.put("234561", "Петрова");
        passportData.put("234432", "Иванов");
        passportData.put("654321", "Петрова");
        passportData.put("345678", "Иванов");
        
        // Вывод данных по сотрудникам с фамилией "Иванов"
        System.out.println("Сотрудники с фамилией Иванов:");
        for (Map.Entry<String, String> entry : passportData.entrySet()) { // Проходим по каждой записи в HashMap
            if (entry.getValue().equals("Иванов")) { // Проверяем, равна ли фамилия "Иванов"
                System.out.println("Номер паспорта: " + entry.getKey() + ", Фамилия: " + entry.getValue());
                // Выводим номер паспорта и фамилию сотрудника
            }
        }
    }
}

